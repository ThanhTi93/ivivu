package model;

/**
 *
 * @author titranthanh
 */
public class Paypal {
    private int id ;
    private int price;
    private int quantity ;
    private String phone ;

    public Paypal(int price, int quantity, String phone) {
        this.price = price;
        this.quantity = quantity;
        this.phone = phone;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
    
    
}
